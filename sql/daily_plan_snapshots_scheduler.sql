-- Scheduler: 00:01 KST every day (UTC 15:01 previous day)
create extension if not exists pg_cron;

do $$
begin
  if exists (select 1 from cron.job where jobname = 'daily-plan-snapshot-kst') then
    perform cron.unschedule('daily-plan-snapshot-kst');
  end if;
end $$;

select cron.schedule(
  'daily-plan-snapshot-kst',
  '1 15 * * *',
  $$select public.capture_daily_plan_snapshot();$$
);
